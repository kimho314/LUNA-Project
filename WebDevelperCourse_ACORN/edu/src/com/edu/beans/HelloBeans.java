package com.edu.beans;

public class HelloBeans
{
	private String name;
	private String number;
	
	public HelloBeans()
	{
		this.name = "no name";
		this.number = "no number";
	}

	public String getName()
	{
		return name;
	}

	public void setName(String name)
	{
		this.name = name;
	}

	public String getNumber()
	{
		return number;
	}

	public void setNumber(String number)
	{
		this.number = number;
	}
	
	
}
