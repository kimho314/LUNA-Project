package day01;

// 2. 산술 연산자

// OperEx2 클래스 시작
public class OperEx2 {
	// main 메소드 시작
	public static void main(String[] args)
	{
		
		// int타입 변수 a,b에 각각 10,20 할당
		int a = 10;
		int b = 20;
		
		// 화면에 a+b의 결과값 출력
		System.out.println(" a + b : " + (a + b));
		// 화면에 a-b의 결과값 출력
		System.out.println(" a - b : " + (a - b));
		// 화면에 a*b 결과값 출력
		System.out.println(" a * b : " + (a * b));
		// 화면에 a/b의 결과값 출력
		System.out.println(" a / b : " + (a / b));
		// 화면에 a%b 결과값 출력
		System.out.println(" a / b : " + (a % b));
	}
}
