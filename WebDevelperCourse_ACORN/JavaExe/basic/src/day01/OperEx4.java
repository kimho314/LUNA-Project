package day01;

// 4. 비교 연산자

// OperEx4 클래스 시작
public class OperEx4 {
	// main 메소드 시작
	public static void main(String[] args)
	{
		// int타입 변수 a,b에 각각 100, 20 할당
		int a = 100;
		int b = 20;
		
		// 화면에 a > b의 결과를 boolean타입으로 출력
		System.out.println(" a > b : " + (a > b));
		// 화면에 a < b의 결과를 boolean타입으로 출력
		System.out.println(" a < b : " + (a < b));
		// 화면에 a <= b의 결과를 boolean타입으로 출력
		System.out.println(" a <= b : " + (a <= b));
		// 화면에 a == b의 결과를 boolean타입으로 출력
		System.out.println(" a == b : " + (a == b));
		// 화면에 a != b의 결과를 boolean타입으로 출력
		System.out.println(" a != b : " + (a != b));
	}
}
