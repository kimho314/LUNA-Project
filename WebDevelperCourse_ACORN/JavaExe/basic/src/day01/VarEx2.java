package day01;

// VarEx2 클래스 시작
public class VarEx2 {
	// main 메소드 시작
	static public void main(String[] args)
	{
		// 자료형 변수명 = 상수;
		
		// int타입 변수 b에 -128할당
		int b = -128;
		// long타입 변수 m에 200할당
		long m = 200L;
		// float타입 변수 f1에 3.141592할당
		float f1 = 3.141592F;
		// double타입 변수 d1에 1.231232할당
		double d1 = 1.231232D;
		
		// 화면어 변수 b에 할당된 값 -128 출력
		System.out.println(" b : " + b);
		// 화면에 변수 m에 할당된 값 200 출력
		System.out.println(" m : " + m);
		// 화면에 변수 f1에 할당된 값 3.141592 출력
		System.out.println(" f1 : " + f1);
		// 화면에 변수 d1에 할당된 값 1.231232 출력
		System.out.println(" d1 : " + d1);
	}
}
