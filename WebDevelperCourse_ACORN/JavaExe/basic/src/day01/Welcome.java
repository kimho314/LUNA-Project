package day01;

// Welcome 클래그 시작
public class Welcome {
	// main 메소드 시작
	public static void main(String[] args) {
		// 문자열 "Welcome"을 표준입력 장치인 화면에 출력
		System.out.println("Welcome");
		// 문자열 "한줄씩"을 표준입력 장치인 화면에 출력
		System.out.println("한줄씩");
		// 문자열 "자례로 실행됩니다."를 화면에 출력
		System.out.println("차례로 실행됩니다.");
	}
}
