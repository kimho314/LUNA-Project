package day01;

// VarEx5 클래스 시작
public class VarEx5 {
	// main 메소드 시작
	public static void main(String[] argc)
	{
		// boolean타입인 변수 b1에 true할당
		boolean b1 = true;
		// boolean타입인 변수 b2에 false할당
		boolean b2 = false;
		
		// 문자열 "b1:"과  b1에 할당된 true을 화면에 출력 
		System.out.println(" b1: " + b1);
		// 문자열 "b2:"과  b2에 할당된 true을 화면에 출력
		System.out.println(" b2: " + b2);
		
		// boolean b3 = 0; (x)
	}
}